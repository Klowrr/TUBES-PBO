    private void btn_simpan_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpan_editActionPerformed
        // TODO add your handling code here:
        String id = tableModel.getValueAt(row, 0).toString();
        String nama = txt_namaService_edit.getText();
        String harga = txt_harga_edit.getText();
        if ((txt_namaService_edit.getText().isEmpty()) || (txt_harga_edit.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong");
            txt_namaService_edit.requestFocus();
        } else {
            try {
                String message = "Are you sure you want to change this data?";
                Object[] options = {"Yes, change it","No, don't change"};
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(database,user,pass);
                Statement stt = kon.createStatement();
                String SQL = "UPDATE `service` SET "
                        +"NAMA = '"+nama+"',"
                        +"HARGA = '"+harga+"'"
                        +"WHERE `ID` = '"+id+"'";
                int choice = JOptionPane.showOptionDialog(null, message, "Change Data", JOptionPane.YES_NO_OPTION , JOptionPane.WARNING_MESSAGE, null, options, options[0]);
                if (choice == JOptionPane.YES_OPTION) {
                    stt.executeUpdate(SQL);
                    data[0] = id;
                    data[1] = nama;                
                    data[2] = harga;  
                    tableModel.removeRow(row);
                    tableModel.insertRow(row, data);
                    stt.close();
                    kon.close();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Di Update");
                    membersihkan_teks();
                    mainPanel.removeAll();
                    mainPanel.add(dataService);
                    mainPanel.repaint();
                    mainPanel.revalidate();
                } else {
                    JOptionPane.showMessageDialog(null, "Canceled");
                }
            } catch (Exception ex) {
                System.err.println(ex.getMessage());
                JOptionPane.showMessageDialog(null,ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpan_editActionPerformed

