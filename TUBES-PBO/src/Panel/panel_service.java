    private void btn_tambah_serviceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambah_serviceActionPerformed
        // TODO add your handling code here:
        String data[] = new String[4];
        if ((txt_namaService.getText().isEmpty()) || txt_harga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
            txt_namaService.requestFocus();
        } else {
            try {
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(database,user,pass);
                Statement stt = kon.createStatement();
                String SQL1 = "SELECT * FROM service WHERE NAMA = '"+txt_namaService.getText()+"' AND STATUS = '1'";
                String message = "Are you sure yo want to add Service data?";
                Object[] options = {"Yes, add Service Data","No, don't add Service data"};
                int choice = JOptionPane.showOptionDialog(null, message, "Add Service Action", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
                if (choice == JOptionPane.YES_OPTION) {
                    ResultSet rs1 = stt.executeQuery(SQL1);
                    if (rs1.next()){
                        JOptionPane.showConfirmDialog(null, "Nama sudah terdaftar");
                    } else {
                        String SQL2 = "SELECT COUNT(*) FROM service";
                        ResultSet countrs = stt.executeQuery(SQL2);
                        countrs.next();
                        int rowCount = countrs.getInt(1);
                        countrs.close();
                        // Generate the ID with leading zeros
                        DecimalFormat idFormat = new DecimalFormat("000");
                        String newID = "SV" + idFormat.format(rowCount + 1);
                        String SQL = "INSERT INTO service (ID,NAMA,HARGA) VALUES('"
                                +newID+"','"
                                +txt_namaService.getText()+"','"
                                +txt_harga.getText()+"')";
                        stt.executeUpdate(SQL);
                        data[0] = newID;
                        data[1] = txt_namaService.getText();
                        data[2] = txt_harga.getText();
                        int tableRowLast = tableModel.getRowCount();
                        tableModel.insertRow(tableRowLast, data);
                        JOptionPane.showMessageDialog(null, "Data Service Berhasil dimasukan");
                        stt.close();
                        kon.close();
                        mainPanel.removeAll();
                        mainPanel.add(dataService);
                        mainPanel.repaint();
                        mainPanel.revalidate();
                        membersihkan_teks();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Canceled");
                }
            } catch (Exception ex) {
                System.err.println(ex.getMessage());
                JOptionPane.showMessageDialog(null,ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_tambah_serviceActionPerformed

