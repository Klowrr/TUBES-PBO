    private void execute() {
        ImageIcon iconBarang = new ImageIcon(getClass().getResource("./img/Product.png"));
        ImageIcon iconTransaksi = new ImageIcon(getClass().getResource("./img/Purchase Order.png"));
        
        menu_item barang1 = new menu_item(null,true,iconBarang,"Barang 1",null);
        menu_item barang2 = new menu_item(null,true,iconBarang,"Barang 2",null);
        menu_item barang3 = new menu_item(null,true,iconBarang,"Barang 3",null);
        
        menu_item transaksi1 = new menu_item(null,true,iconTransaksi,"History Transaksi1",null);
        menu_item transaksi2 = new menu_item(null,true,iconTransaksi,"History Transaksi2",null);
        menu_item transaksi3 = new menu_item(null,true,iconTransaksi,"History Transaksi3",null);


        menu_item menuBarang = new menu_item(iconBarang,false,null,"Barang",null,barang1,barang2,barang3);
        menu_item menuTransaksi = new menu_item(iconBarang,false,null,"Transaksi",null,transaksi1,transaksi2,transaksi3);
        menu_item menuClient = new menu_item(iconBarang,false,null,"Client",null);

        addMenu(menuBarang,menuClient,menuTransaksi);
    }
    private void addMenu(menu_item... menu){    
        for (int i = 0; i<menu.length;i++){
            pn_menu.add(menu[i]);
            ArrayList<menu_item> subMenu = menu[i].getSubMenu();
            for (menu_item m : subMenu){
                addMenu(m);
            }
        }
        pn_menu.revalidate();
    }
